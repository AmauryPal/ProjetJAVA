package fr;

public class Player {
	
	private int food = 1000;
	private int x = 0;
	private int y = 0;
	
	public Player() {}
	
	
	
	public int getFood() {
		return food;
	}
	public void setFood(int food) {
		this.food = food;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}

}
